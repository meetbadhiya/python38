## Python program to demonstrate basic data types

# Integer
integer_num = 10
print("Integer:", integer_num)
print("Type of integer_num:", type(integer_num))

# Float
float_num = 3.14
print("\nFloat:", float_num)
print("Type of float_num:", type(float_num))

# Complex
complex_num = 2 + 3j
print("\nComplex:", complex_num)
print("Type of complex_num:", type(complex_num))

# Boolean
bool_value = True
print("\nBoolean:", bool_value)
print("Type of bool_value:", type(bool_value))

# String
string_value = "Hello, Python!"
print("\nString:", string_value)
print("Type of string_value:", type(string_value))
